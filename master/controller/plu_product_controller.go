type PluProductController struct {
	PluProductService service.PluProductService
	validator         *validation.Validate
}

func NewPluProductController(pluProductService service.PluProductService, validator *validation.Validate) *PluProductController {
	return &PluProductController{
		PluProductService: pluProductService,
		validator:         validator,
	}
}

func (controller *PluProductController) Route(app *fiber.App) {
	qParamId := ":plu_pro_id"
	pluProductsRouteV1 := app.Group("/v1/plu-products", middleware.JWTProtected())
	pluProductsRouteV1.Get("/"+qParamId, controller.Detail)
	pluProductsRouteV1.Get("", controller.List)
	pluProductsRouteV1.Post("", controller.Create)
	pluProductsRouteV1.Patch("/"+qParamId, controller.Update)
	pluProductsRouteV1.Delete("/"+qParamId, controller.Delete)
}

func (controller *PluProductController) Detail(c *fiber.Ctx) error {
	var params entity.DetailPluProductParams
	var headerAcceptLang string
	if len(c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG]) > 0 {
		headerAcceptLang = c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG][0]
	}
	responsePayload := responsebuild.BuildResponse(c.Locals("requestid").(string), headerAcceptLang)

	if err := c.ParamsParser(&params); err != nil {
		log.Println("PluProductController, Detail, ParamsParser:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusUnprocessableEntity).JSON(responsePayload.GetRespPayload())
	}

	errs := controller.validator.ValidateStruct(params, headerAcceptLang)
	if errs != nil {
		log.Println("PluProductController, Detail, ValidateStruct(params), errs:", errs)
		responsePayload.Setmsg(fiber.ErrBadRequest.Message)
		responsePayload.Seterrors(errs)
		return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
	}

	custId := c.Locals("cust_id").(string)
	// log.Println("PluProductController, Detail, CustId:", custId)

	data, err := controller.PluProductService.Detail(params.PluProId, custId)
	if err != nil {
		log.Println("PluProductController, Detail, FindOneByPluGroupId, err:", err.Error())
		statusCode := fiber.StatusBadRequest
		errMsg := err.Error()
		if err.Error() == "sql: no rows in result set" {
			statusCode = fiber.StatusNotFound
			errMsg = "Not found"
		}
		responsePayload.Setmsg(errMsg)
		return c.Status(statusCode).JSON(responsePayload.GetRespPayload())
	}

	responsePayload.Setdata(data)
	return c.JSON(responsePayload.GetRespPayload())
}

