type MParkingFundController struct {
	MParkingFundService service.MParkingFundService
	validator           *validation.Validate
}

func NewMParkingFundController(mParkingFundService service.MParkingFundService, validator *validation.Validate) *MParkingFundController {
	return &MParkingFundController{
		MParkingFundService: mParkingFundService,
		validator:           validator,
	}
}

func (controller *MParkingFundController) Route(app *fiber.App) {

	qParamId := ":parking_fund_id"
	// qParamId2 := ":pro_id"
	discProductRouteV1 := app.Group("/v1/parking-fund", middleware.JWTProtected())
	discProductRouteV1.Get("/"+qParamId, controller.Detail)
	discProductRouteV1.Get("", controller.List)
	discProductRouteV1.Post("", controller.Create)
	discProductRouteV1.Patch("/"+qParamId, controller.Update)
	discProductRouteV1.Delete("/"+qParamId, controller.Delete)
}

func (controller *MParkingFundController) Detail(c *fiber.Ctx) error {
	var params entity.DetailMParkingFundParams
	var headerAcceptLang string
	if len(c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG]) > 0 {
		headerAcceptLang = c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG][0]
	}
	responsePayload := responsebuild.BuildResponse(c.Locals("requestid").(string), headerAcceptLang)

	if err := c.ParamsParser(&params); err != nil {
		log.Println("MParkingFundController, Detail, ParamsParser:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusUnprocessableEntity).JSON(responsePayload.GetRespPayload())
	}

	errs := controller.validator.ValidateStruct(params, headerAcceptLang)
	if errs != nil {
		log.Println("MParkingFundController, Detail, ValidateStruct(params), errs:", errs)
		responsePayload.Setmsg(fiber.ErrBadRequest.Message)
		responsePayload.Seterrors(errs)
		return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
	}

	custId := c.Locals("cust_id").(string)
	// log.Println("MParkingFundController, Detail, CustId:", custId)

	data, err := controller.MParkingFundService.Detail(params.ParkingFundId, custId)
	if err != nil {
		log.Println("MParkingFundController, Detail, err:", err.Error())
		statusCode := fiber.StatusBadRequest
		errMsg := err.Error()
		if err.Error() == "sql: no rows in result set" {
			statusCode = fiber.StatusNotFound
			errMsg = "Not found"
		}

		responsePayload.Setmsg(errMsg)
		return c.Status(statusCode).JSON(responsePayload.GetRespPayload())
	}

	responsePayload.Setdata(data)
	return c.JSON(responsePayload.GetRespPayload())
}

func (controller *MParkingFundController) List(c *fiber.Ctx) error {
	var dataFilter entity.GeneralQueryFilter
	var headerAcceptLang string
	if len(c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG]) > 0 {
		headerAcceptLang = c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG][0]
	}
	responsePayload := responsebuild.BuildResponse(c.Locals("requestid").(string), headerAcceptLang)

	if err := c.QueryParser(&dataFilter); err != nil {
		log.Println("MParkingFundController, List, query parser filter:", err.Error())
	}

	custId := c.Locals("cust_id").(string)
	// log.Println("MParkingFundController, List, CustId:", custId)

	data, total, lastPage, err := controller.MParkingFundService.List(dataFilter, custId)
	if err != nil {
		log.Println("MParkingFundController, List, data, err:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
	}

	responsePayload.Setdata(data)
	responsePayload.Setpaging(entity.Pagination{
		TotalRecord: total,
		PageCurrent: dataFilter.Page,
		PageLimit:   dataFilter.Limit,
		PageTotal:   lastPage,
	})

	return c.Status(fiber.StatusOK).JSON(responsePayload.GetRespPayload())
}

func (controller *MParkingFundController) Create(c *fiber.Ctx) error {
	var request entity.CreateMParkingFundBody
	var headerAcceptLang string
	if len(c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG]) > 0 {
		headerAcceptLang = c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG][0]
	}
	responsePayload := responsebuild.BuildResponse(c.Locals("requestid").(string), headerAcceptLang)

	if err := c.BodyParser(&request); err != nil {
		log.Println("MParkingPromoController, Create, BodyParser:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusUnprocessableEntity).JSON(responsePayload.GetRespPayload())
	}

	custId := c.Locals("cust_id").(string)
	userId := c.Locals("user_id").(int64)

	// log.Println("MParkingPromoController, Create, CustId:", custId)

	request.CustId = custId
	request.CreatedBy = userId
	request.UpdatedBy = userId

	errs := controller.validator.ValidateStruct(request, headerAcceptLang)
	if errs != nil {
		log.Println("MParkingPromoController, Create, ValidateStruct, errs:", errs)
		responsePayload.Setmsg(fiber.ErrBadRequest.Message)
		responsePayload.Seterrors(errs)
		return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
	}

	_, err := controller.MParkingFundService.Store(request)
	if err != nil {
		log.Println("MParkingPromoController, Create, Store, err:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
	}

	responsePayload.Setmsg(constant.SUCCESSFULLY_ADDED)
	return c.Status(fiber.StatusCreated).JSON(responsePayload.GetRespPayload())
}

