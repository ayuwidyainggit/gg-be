func (controller *ReturnReasonDistributorController) Route(app *fiber.App) {
	qParamId := ":return_reason_id"
	returnReasonsDistributorsRouteV1 := app.Group("/v1/return-reasons-distributor", middleware.JWTProtected())
	returnReasonsDistributorsRouteV1.Get("/"+qParamId, controller.Detail)
	returnReasonsDistributorsRouteV1.Get("", controller.List)
	returnReasonsDistributorsRouteV1.Post("", controller.Create)
	returnReasonsDistributorsRouteV1.Patch("/"+qParamId, controller.Update)
	returnReasonsDistributorsRouteV1.Delete("/"+qParamId, controller.Delete)
}

func (controller *ReturnReasonDistributorController) Detail(c *fiber.Ctx) error {
	var params entity.DetailReturnReasonDistributorParams
	var headerAcceptLang string
	if len(c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG]) > 0 {
		headerAcceptLang = c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG][0]
	}
	responsePayload := responsebuild.BuildResponse(c.Locals("requestid").(string), headerAcceptLang)

	if err := c.ParamsParser(&params); err != nil {
		log.Println("ReturnReasonDistributorController, Detail, ParamsParser:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusUnprocessableEntity).JSON(responsePayload.GetRespPayload())
	}

	errs := controller.validator.ValidateStruct(params, headerAcceptLang)
	if errs != nil {
		log.Println("ReturnReasonDistributorController, Detail, ValidateStruct(params), errs:", errs)
		responsePayload.Setmsg(fiber.ErrBadRequest.Message)
		responsePayload.Seterrors(errs)
		return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
	}

	custId := c.Locals("parent_cust_id").(string)
	data, err := controller.ReturnReasonDistributorService.Detail(params.ReturnReasonDistributorId, custId)
	if err != nil {
		log.Println("ReturnReasonDistributorController, Detail, FindOneByReturnReasonDistributorId, err:", err.Error())
		statusCode := fiber.StatusBadRequest
		errMsg := err.Error()
		if err.Error() == "sql: no rows in result set" {
			statusCode = fiber.StatusNotFound
			errMsg = "Not found"
		}
		responsePayload.Setmsg(errMsg)
		return c.Status(statusCode).JSON(responsePayload.GetRespPayload())
	}

	responsePayload.Setdata(data)
	return c.JSON(responsePayload.GetRespPayload())
}

func (controller *ReturnReasonDistributorController) List(c *fiber.Ctx) error {
	var (
		err                error
		dataFilter         entity.ReturnReasonDistributorQueryFilter
		data               interface{}
		total              int
		lastPage           int
		returnReason       []entity.ReturnReasonDistributorResponse
		returnReasonLookup []entity.ReturnReasonDistributorLookupResponse
	)

	var headerAcceptLang string
	if len(c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG]) > 0 {
		headerAcceptLang = c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG][0]
	}
	responsePayload := responsebuild.BuildResponse(c.Locals("requestid").(string), headerAcceptLang)
	if err := c.QueryParser(&dataFilter); err != nil {
		log.Println("ReturnReasonDistributorController, List, query parser filter:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusUnprocessableEntity).JSON(responsePayload.GetRespPayload())
	}

	custId := c.Locals("parent_cust_id").(string)

	switch dataFilter.Mode {
	case "lookup":
		data, total, lastPage, err = controller.ReturnReasonDistributorService.LookupList(dataFilter, custId)
		if err != nil {
			log.Println("ReturnReasonDistributorController, List, data, err:", err.Error())
			responsePayload.Setmsg(err.Error())
			return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
		}
		err = structs.Automapper(returnReasonLookup, &data)
		if err != nil {
			log.Println("ReturnReasonDistributorController, List, data, err:", err.Error())
			responsePayload.Setmsg(err.Error())
			return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
		}
	default:
		data, total, lastPage, err = controller.ReturnReasonDistributorService.List(dataFilter, custId)
		if err != nil {
			log.Println("ReturnReasonDistributorController, List, data, err:", err.Error())
			responsePayload.Setmsg(err.Error())
			return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
		}
		err = structs.Automapper(returnReason, &data)
		if err != nil {
			log.Println("ReturnReasonDistributorController, List, data, err:", err.Error())
			responsePayload.Setmsg(err.Error())
			return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
		}
	}

	responsePayload.Setdata(data)
	responsePayload.Setpaging(entity.Pagination{
		TotalRecord: total,
		PageCurrent: dataFilter.Page,
		PageLimit:   dataFilter.Limit,
		PageTotal:   lastPage,
	})

	return c.Status(fiber.StatusOK).JSON(responsePayload.GetRespPayload())
}

