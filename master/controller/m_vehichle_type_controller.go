type VehicleTypeController struct {
	VehicleTypeService service.VehicleTypeService
	validator          *validation.Validate
}

func NewVehicleTypeController(vehicleTypeService service.VehicleTypeService, validator *validation.Validate) VehicleTypeController {
	return VehicleTypeController{
		VehicleTypeService: vehicleTypeService,
		validator:          validator,
	}
}

func (controller *VehicleTypeController) Route(app *fiber.App) {
	qParamId := ":vehicle_type_id"
	vehicleTypeRouteV1 := app.Group("/v1/vehicle-types", middleware.JWTProtected())
	vehicleTypeRouteV1.Get("/"+qParamId, controller.Detail)
	vehicleTypeRouteV1.Get("", controller.List)
	vehicleTypeRouteV1.Post("", controller.Create)
	vehicleTypeRouteV1.Patch("/"+qParamId, controller.Update)
	vehicleTypeRouteV1.Delete("/"+qParamId, controller.Delete)
}

func (controller *VehicleTypeController) Detail(c *fiber.Ctx) error {
	var params entity.DetailVehicletypeParams
	var headerAcceptLang string
	if len(c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG]) > 0 {
		headerAcceptLang = c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG][0]
	}
	responsePayload := responsebuild.BuildResponse(c.Locals("requestid").(string), headerAcceptLang)

	if err := c.ParamsParser(&params); err != nil {
		log.Println("VehicleTypeController, Detail, ParamsParser:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusUnprocessableEntity).JSON(responsePayload.GetRespPayload())
	}

	errs := controller.validator.ValidateStruct(params, headerAcceptLang)
	if errs != nil {
		log.Println("VehicleTypeController, Detail, ValidateStruct(params), errs:", errs)
		responsePayload.Setmsg(fiber.ErrBadRequest.Message)
		responsePayload.Seterrors(errs)
		return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
	}

	custId := c.Locals("parent_cust_id").(string)
	// log.Println("VehicleTypeController, Detail, CustId:", custId)

	data, err := controller.VehicleTypeService.Detail((params.VehicleTypeId), custId)
	if err != nil {
		log.Println("VehicleTypeController, Detail, FindOneByVehicleTypeId, err:", err.Error())
		statusCode := fiber.StatusBadRequest
		errMsg := err.Error()
		if err.Error() == "sql: no rows in result set" {
			statusCode = fiber.StatusNotFound
			errMsg = "Not found"
		}
		responsePayload.Setmsg(errMsg)
		return c.Status(statusCode).JSON(responsePayload.GetRespPayload())
	}

	responsePayload.Setdata(data)
	return c.JSON(responsePayload.GetRespPayload())
}

func (controller *VehicleTypeController) List(c *fiber.Ctx) error {
	var (
		err        error
		dataFilter entity.VehicletypeQueryFilter
		data       interface{}
		total      int
		lastPage   int
	)
	var headerAcceptLang string
	if len(c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG]) > 0 {
		headerAcceptLang = c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG][0]
	}
	responsePayload := responsebuild.BuildResponse(c.Locals("requestid").(string), headerAcceptLang)

	if err := c.QueryParser(&dataFilter); err != nil {
		log.Println("VehicleTypeController, List, query parser filter:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusUnprocessableEntity).JSON(responsePayload.GetRespPayload())
	}

	custId := c.Locals("cust_id").(string)
	// log.Println("VehicleTypeController, List, CustId:", custId)

	switch dataFilter.Mode {
	case "lookup":
		custId = c.Locals("parent_cust_id").(string)
		data, total, lastPage, err = controller.VehicleTypeService.LookupList(dataFilter, custId)
		if err != nil {
			log.Println("VehicleTypeController, LookupList, data, err:", err.Error())
			responsePayload.Setmsg(err.Error())
			return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
		}
	default:
		data, total, lastPage, err = controller.VehicleTypeService.List(dataFilter, custId)
		if err != nil {
			log.Println("VehicleTypeController, List, data, err:", err.Error())
			responsePayload.Setmsg(err.Error())
			return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
		}
	}

	responsePayload.Setdata(data)
	responsePayload.Setpaging(entity.Pagination{
		TotalRecord: total,
		PageCurrent: dataFilter.Page,
		PageLimit:   dataFilter.Limit,
		PageTotal:   lastPage,
	})

	return c.Status(fiber.StatusOK).JSON(responsePayload.GetRespPayload())
}

func (controller *VehicleTypeController) Create(c *fiber.Ctx) error {
	var headerAcceptLang string
	if len(c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG]) > 0 {
		headerAcceptLang = c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG][0]
	}
	responsePayload := responsebuild.BuildResponse(c.Locals("requestid").(string), headerAcceptLang)
	var request entity.CreateVehicletypeBody
	if err := c.BodyParser(&request); err != nil {
		log.Println("VehicleTypeController, Create, BodyParser:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusUnprocessableEntity).JSON(responsePayload.GetRespPayload())
	}

	custId := c.Locals("cust_id").(string)
	userId := c.Locals("user_id").(int64)
	// log.Println("VehicleTypeController, Create, CustId:", custId)

	request.CustId = custId
	request.CreatedBy = userId

	errs := controller.validator.ValidateStruct(request, headerAcceptLang)
	if errs != nil {
		log.Println("VehicleTypeController, Create, ValidateStruct, errs:", errs)
		responsePayload.Setmsg(fiber.ErrBadRequest.Message)
		responsePayload.Seterrors(errs)
		return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
	}

	_, err := controller.VehicleTypeService.Store(request)
	if err != nil {
		log.Println("VehicleTypeController, Create, Store, err:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
	}

	responsePayload.Setmsg(constant.SUCCESSFULLY_ADDED)
	return c.Status(fiber.StatusCreated).JSON(responsePayload.GetRespPayload())
}

