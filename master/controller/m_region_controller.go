type RegionController struct {
	RegionService service.RegionService
	validator     *validation.Validate
}

func NewRegionController(regionService service.RegionService, validator *validation.Validate) *RegionController {
	return &RegionController{
		RegionService: regionService,
		validator:     validator,
	}
}

func (controller *RegionController) Route(app *fiber.App) {
	qParamId := ":region_id"
	regionsRouteV1 := app.Group("/v1/regions", middleware.JWTProtected())
	regionsRouteV1.Get("/"+qParamId, controller.Detail)
	regionsRouteV1.Get("", controller.List)
	regionsRouteV1.Post("", controller.Create)
	regionsRouteV1.Patch("/"+qParamId, controller.Update)
	regionsRouteV1.Delete("/"+qParamId, controller.Delete)
}

func (controller *RegionController) Detail(c *fiber.Ctx) error {
	var params entity.DetailRegionParams
	var headerAcceptLang string
	if len(c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG]) > 0 {
		headerAcceptLang = c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG][0]
	}
	responsePayload := responsebuild.BuildResponse(c.Locals("requestid").(string), headerAcceptLang)

	if err := c.ParamsParser(&params); err != nil {
		log.Println("RegionController, Detail, ParamsParser:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusUnprocessableEntity).JSON(responsePayload.GetRespPayload())
	}

	errs := controller.validator.ValidateStruct(params, headerAcceptLang)
	if errs != nil {
		log.Println("RegionController, Detail, ValidateStruct(params), errs:", errs)
		responsePayload.Setmsg(fiber.ErrBadRequest.Message)
		responsePayload.Seterrors(errs)
		return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
	}

	custId := c.Locals("cust_id").(string)
	// log.Println("RegionController, Detail, CustId:", custId)

	data, err := controller.RegionService.Detail(params.RegionId, custId)
	if err != nil {
		log.Println("RegionController, Detail, FindOneByRegionId, err:", err.Error())
		statusCode := fiber.StatusBadRequest
		errMsg := err.Error()
		if err.Error() == "sql: no rows in result set" {
			statusCode = fiber.StatusNotFound
			errMsg = "Not found"
		}
		responsePayload.Setmsg(errMsg)
		return c.Status(statusCode).JSON(responsePayload.GetRespPayload())
	}

	responsePayload.Setdata(data)
	return c.JSON(responsePayload.GetRespPayload())
}

