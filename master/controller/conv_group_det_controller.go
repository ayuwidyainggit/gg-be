type ConvGroupDetController struct {
	ConvGroupDetService service.ConvGroupDetService
	validator           *validation.Validate
}

func NewConvGroupDetController(convGroupDetService service.ConvGroupDetService, validator *validation.Validate) *ConvGroupDetController {
	return &ConvGroupDetController{
		ConvGroupDetService: convGroupDetService,
		validator:           validator,
	}
}

func (controller *ConvGroupDetController) Route(app *fiber.App) {
	qParamId1 := ":conv_grp_id"
	qParamId2 := ":pro_id"
	convGroupsDetRouteV1 := app.Group("/v1/conv-groups-det", middleware.JWTProtected())
	convGroupsDetRouteV1.Get("/"+qParamId1+"/"+qParamId2, controller.Detail)
	convGroupsDetRouteV1.Get("", controller.List)
	convGroupsDetRouteV1.Post("", controller.Create)
	convGroupsDetRouteV1.Patch("/"+qParamId1+"/"+qParamId2, controller.Update)
	convGroupsDetRouteV1.Delete("/"+qParamId1+"/"+qParamId2, controller.Delete)
}

func (controller *ConvGroupDetController) Detail(c *fiber.Ctx) error {
	var params entity.DetailConvGroupDetParams
	var headerAcceptLang string
	if len(c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG]) > 0 {
		headerAcceptLang = c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG][0]
	}
	responsePayload := responsebuild.BuildResponse(c.Locals("requestid").(string), headerAcceptLang)

	if err := c.ParamsParser(&params); err != nil {
		log.Println("ConvGroupDetController, Detail, ParamsParser:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusUnprocessableEntity).JSON(responsePayload.GetRespPayload())
	}

	errs := controller.validator.ValidateStruct(params, headerAcceptLang)
	if errs != nil {
		log.Println("ConvGroupDetController, Detail, ValidateStruct(params), errs:", errs)
		responsePayload.Setmsg(fiber.ErrBadRequest.Message)
		responsePayload.Seterrors(errs)
		return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
	}

	custId := c.Locals("cust_id").(string)
	// log.Println("ConvGroupDetController, Detail, CustId:", custId)

	data, err := controller.ConvGroupDetService.Detail(params.ConvGrpId, params.ProId, custId)
	if err != nil {
		log.Println("ConvGroupDetController, Detail, FindOneByConvGroupId, err:", err.Error())
		statusCode := fiber.StatusBadRequest
		errMsg := err.Error()
		if err.Error() == "sql: no rows in result set" {
			statusCode = fiber.StatusNotFound
			errMsg = "Not found"
		}
		responsePayload.Setmsg(errMsg)
		return c.Status(statusCode).JSON(responsePayload.GetRespPayload())
	}

	responsePayload.Setdata(data)
	return c.JSON(responsePayload.GetRespPayload())
}

func (controller *ConvGroupDetController) List(c *fiber.Ctx) error {
	var dataFilter entity.GeneralQueryFilter
	var headerAcceptLang string
	if len(c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG]) > 0 {
		headerAcceptLang = c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG][0]
	}
	responsePayload := responsebuild.BuildResponse(c.Locals("requestid").(string), headerAcceptLang)

	if err := c.QueryParser(&dataFilter); err != nil {
		log.Println("ConvGroupDetController, List, query parser filter:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusUnprocessableEntity).JSON(responsePayload.GetRespPayload())
	}

	custId := c.Locals("cust_id").(string)
	// log.Println("ConvGroupDetController, List, CustId:", custId)

	data, total, lastPage, err := controller.ConvGroupDetService.List(dataFilter, custId)
	if err != nil {
		log.Println("ConvGroupDetController, List, data, err:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
	}

	responsePayload.Setdata(data)
	responsePayload.Setpaging(entity.Pagination{
		TotalRecord: total,
		PageCurrent: dataFilter.Page,
		PageLimit:   dataFilter.Limit,
		PageTotal:   lastPage,
	})

	return c.Status(fiber.StatusOK).JSON(responsePayload.GetRespPayload())
}

