type UnitCoreTaxController struct {
	UnitCoreTaxService service.UnitCoreTaxService
	validator          *validation.Validate
}

func NewUnitCoreTaxController(coreTaxService service.UnitCoreTaxService, validator *validation.Validate) *UnitCoreTaxController {
	return &UnitCoreTaxController{
		UnitCoreTaxService: coreTaxService,
		validator:          validator,
	}
}

func (controller *UnitCoreTaxController) Route(app *fiber.App) {
	qParamId := ":unit_id_coretax"
	coreTaxsRouteV1 := app.Group("/v1/unit-coretax", middleware.JWTProtected())
	coreTaxsRouteV1.Get("/"+qParamId, controller.Detail)
	coreTaxsRouteV1.Get("", controller.List)
	coreTaxsRouteV1.Post("", controller.Create)
	coreTaxsRouteV1.Patch("/"+qParamId, controller.Update)
	coreTaxsRouteV1.Delete("/"+qParamId, controller.Delete)
}

func (controller *UnitCoreTaxController) Detail(c *fiber.Ctx) error {
	var params entity.DetailUnitCoreTaxParams
	var headerAcceptLang string
	if len(c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG]) > 0 {
		headerAcceptLang = c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG][0]
	}
	responsePayload := responsebuild.BuildResponse(c.Locals("requestid").(string), headerAcceptLang)

	if err := c.ParamsParser(&params); err != nil {
		log.Println("UnitCoreTaxController, Detail, ParamsParser:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusUnprocessableEntity).JSON(responsePayload.GetRespPayload())
	}

	errs := controller.validator.ValidateStruct(params, headerAcceptLang)
	if errs != nil {
		log.Println("UnitCoreTaxController, Detail, ValidateStruct(params), errs:", errs)
		responsePayload.Setmsg(fiber.ErrBadRequest.Message)
		responsePayload.Seterrors(errs)
		return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
	}

	custId := c.Locals("parent_cust_id").(string)
	data, err := controller.UnitCoreTaxService.Detail(params.UnitIdCoreTax, custId)
	if err != nil {
		log.Println("UnitCoreTaxController, Detail, FindOneByUnitIdCoreTax, err:", err.Error())
		statusCode := fiber.StatusBadRequest
		errMsg := err.Error()
		if err.Error() == "sql: no rows in result set" {
			statusCode = fiber.StatusNotFound
			errMsg = "Not found"
		}
		responsePayload.Setmsg(errMsg)
		return c.Status(statusCode).JSON(responsePayload.GetRespPayload())
	}

	responsePayload.Setdata(data)
	return c.JSON(responsePayload.GetRespPayload())
}

func (controller *UnitCoreTaxController) List(c *fiber.Ctx) error {
	var dataFilter entity.GeneralQueryFilter
	var headerAcceptLang string
	if len(c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG]) > 0 {
		headerAcceptLang = c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG][0]
	}
	responsePayload := responsebuild.BuildResponse(c.Locals("requestid").(string), headerAcceptLang)

	if err := c.QueryParser(&dataFilter); err != nil {
		log.Println("UnitCoreTaxController, List, query parser filter:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusUnprocessableEntity).JSON(responsePayload.GetRespPayload())

	}

	custId := c.Locals("parent_cust_id").(string)
	data, total, lastPage, err := controller.UnitCoreTaxService.List(dataFilter, custId)
	if err != nil {
		log.Println("UnitCoreTaxController, List, data, err:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
	}

	responsePayload.Setdata(data)
	responsePayload.Setpaging(entity.Pagination{
		TotalRecord: total,
		PageCurrent: dataFilter.Page,
		PageLimit:   dataFilter.Limit,
		PageTotal:   lastPage,
	})

	return c.Status(fiber.StatusOK).JSON(responsePayload.GetRespPayload())

}

