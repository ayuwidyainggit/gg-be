type SpecialPriceGroupController struct {
	SpecialPriceGroupService service.SpecialPriceGroupService
	validator                *validation.Validate
}

func NewSpecialPriceGroupController(specialPriceGroupService service.SpecialPriceGroupService, validator *validation.Validate) SpecialPriceGroupController {
	return SpecialPriceGroupController{
		SpecialPriceGroupService: specialPriceGroupService,
		validator:                validator,
	}
}

func (controller *SpecialPriceGroupController) Route(app *fiber.App) {
	qParamId := ":sp_price_grp_id"
	specialPriceGroupRouteV1 := app.Group("/v1/special-price-group", middleware.JWTProtected())
	specialPriceGroupRouteV1.Get("/"+qParamId, controller.Detail)
	specialPriceGroupRouteV1.Get("", controller.List)
	specialPriceGroupRouteV1.Post("", controller.Create)
	specialPriceGroupRouteV1.Patch("/"+qParamId, controller.Update)
	specialPriceGroupRouteV1.Delete("/"+qParamId, controller.Delete)
}

func (controller *SpecialPriceGroupController) Detail(c *fiber.Ctx) error {
	var params entity.DetailSpecialPriceGroupParams
	var headerAcceptLang string
	if len(c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG]) > 0 {
		headerAcceptLang = c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG][0]
	}
	responsePayload := responsebuild.BuildResponse(c.Locals("requestid").(string), headerAcceptLang)

	if err := c.ParamsParser(&params); err != nil {
		log.Println("SpecialPriceGroupController, Detail, ParamsParser:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusUnprocessableEntity).JSON(responsePayload.GetRespPayload())
	}

	errs := controller.validator.ValidateStruct(params, headerAcceptLang)
	if errs != nil {
		log.Println("SpecialPriceGroupController, Detail, ValidateStruct(params), errs:", errs)
		responsePayload.Setmsg(fiber.ErrBadRequest.Message)
		responsePayload.Seterrors(errs)
		return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
	}

	custId := c.Locals("cust_id").(string)
	// log.Println("SpecialPriceGroupController, Detail, CustId:", custId)

	data, err := controller.SpecialPriceGroupService.Detail((params.SpecialPriceGroupId), custId)
	if err != nil {
		log.Println("SpecialPriceGroupController, Detail, FindOneBySpecialPriceGroupId, err:", err.Error())
		statusCode := fiber.StatusBadRequest
		errMsg := err.Error()
		if err.Error() == "sql: no rows in result set" {
			statusCode = fiber.StatusNotFound
			errMsg = "Not found"
		}
		responsePayload.Setmsg(errMsg)
		return c.Status(statusCode).JSON(responsePayload.GetRespPayload())
	}

	responsePayload.Setdata(data)
	return c.JSON(responsePayload.GetRespPayload())
}

func (controller *SpecialPriceGroupController) List(c *fiber.Ctx) error {
	var (
		err        error
		dataFilter entity.SpecialPriceGroupQueryFilter
		data       interface{}
		total      int
		lastPage   int
	)
	var headerAcceptLang string
	if len(c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG]) > 0 {
		headerAcceptLang = c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG][0]
	}
	responsePayload := responsebuild.BuildResponse(c.Locals("requestid").(string), headerAcceptLang)

	if err := c.QueryParser(&dataFilter); err != nil {
		log.Println("SpecialPriceGroupController, List, query parser filter:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusUnprocessableEntity).JSON(responsePayload.GetRespPayload())
	}

	custId := c.Locals("cust_id").(string)
	// log.Println("SpecialPriceGroupController, List, CustId:", custId)

	switch dataFilter.Mode {
	case "lookup":
		data, total, lastPage, err = controller.SpecialPriceGroupService.LookupList(dataFilter, custId)
		if err != nil {
			log.Println("SpecialPriceGroupController, LookupList, data, err:", err.Error())
			responsePayload.Setmsg(err.Error())
			return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
		}
	default:
		data, total, lastPage, err = controller.SpecialPriceGroupService.List(dataFilter, custId)
		if err != nil {
			log.Println("SpecialPriceGroupController, List, data, err:", err.Error())
			responsePayload.Setmsg(err.Error())
			return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
		}
	}

	responsePayload.Setdata(data)
	responsePayload.Setpaging(entity.Pagination{
		TotalRecord: total,
		PageCurrent: dataFilter.Page,
		PageLimit:   dataFilter.Limit,
		PageTotal:   lastPage,
	})

	return c.Status(fiber.StatusOK).JSON(responsePayload.GetRespPayload())
}

