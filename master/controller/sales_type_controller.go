type SalesTypeController struct {
	SalesTypeService service.SalesTypeService
	validator        *validation.Validate
}

func NewSalesTypeController(salesTypeService service.SalesTypeService, validator *validation.Validate) *SalesTypeController {
	return &SalesTypeController{
		SalesTypeService: salesTypeService,
		validator:        validator,
	}
}

func (controller *SalesTypeController) Route(app *fiber.App) {
	qParamId := ":sales_type_id"
	groupV1 := app.Group("/v1/sales-types", middleware.JWTProtected())
	groupV1.Get("/"+qParamId, controller.Detail)
	groupV1.Get("", controller.List)
	groupV1.Post("", controller.Create)
	groupV1.Patch("/"+qParamId, controller.Update)
	groupV1.Delete("/"+qParamId, controller.Delete)
}

func (controller *SalesTypeController) Detail(c *fiber.Ctx) error {
	var params entity.DetailSalesTypeParams
	var headerAcceptLang string
	if len(c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG]) > 0 {
		headerAcceptLang = c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG][0]
	}
	responsePayload := responsebuild.BuildResponse(c.Locals("requestid").(string), headerAcceptLang)

	if err := c.ParamsParser(&params); err != nil {
		log.Println("SalesTypeController, Detail, ParamsParser:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusUnprocessableEntity).JSON(responsePayload.GetRespPayload())
	}

	errs := controller.validator.ValidateStruct(params, headerAcceptLang)
	if errs != nil {
		log.Println("SalesTypeController, Detail, ValidateStruct(params), errs:", errs)
		responsePayload.Setmsg(fiber.ErrBadRequest.Message)
		responsePayload.Seterrors(errs)
		return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
	}

	custId := c.Locals("cust_id").(string)
	// log.Println("SalesTypeController, Detail, CustId:", custId)

	data, err := controller.SalesTypeService.Detail(params.SalesTypeId, custId)
	if err != nil {
		log.Println("SalesTypeController, Detail, FindOneBySalesTypeId, err:", err.Error())
		statusCode := fiber.StatusBadRequest
		errMsg := err.Error()
		if err.Error() == "sql: no rows in result set" {
			statusCode = fiber.StatusNotFound
			errMsg = "Not found"
		}
		responsePayload.Setmsg(errMsg)
		return c.Status(statusCode).JSON(responsePayload.GetRespPayload())
	}

	responsePayload.Setdata(data)
	return c.JSON(responsePayload.GetRespPayload())
}

