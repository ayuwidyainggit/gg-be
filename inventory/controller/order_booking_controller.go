type OrderController struct {
	OrderService service.OrderService
	validator    *validation.Validate
}

func NewOrderBookingController(orderService service.OrderService, validator *validation.Validate) *OrderController {
	return &OrderController{
		OrderService: orderService,
		validator:    validator,
	}
}

func (controller *OrderController) Route(app *fiber.App) {
	qParamId := ":order_booking_id"
	orderBookingRouteV1 := app.Group("/v1/order-booking", middleware.JWTProtected())
	orderBookingRouteV1.Post("", controller.Create)
	orderBookingRouteV1.Get("/status", controller.LookupStatus)
	orderBookingRouteV1.Get("/approval", controller.ListApproval)
	orderBookingRouteV1.Get("/"+qParamId, controller.Detail)
	orderBookingRouteV1.Put("/reject/"+qParamId, controller.UpdateReject)
	orderBookingRouteV1.Put("/approve/"+qParamId, controller.UpdateApprove)
	orderBookingRouteV1.Get("", controller.List)
	orderBookingRouteV1.Delete("/"+qParamId, controller.Delete)
}

func (controller *OrderController) Create(c *fiber.Ctx) error {
	var headerAcceptLang string
	if len(c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG]) > 0 {
		headerAcceptLang = c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG][0]
	}
	responsePayload := responsebuild.BuildResponse(c.Locals("requestid").(string), headerAcceptLang)

	var request entity.CreateOrderBody
	if err := c.BodyParser(&request); err != nil {
		log.Error("OrderController, Create, BodyParser:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusUnprocessableEntity).JSON(responsePayload.GetRespPayload())
	}

	custId := c.Locals("cust_id").(string)
	parentCustId := c.Locals("parent_cust_id").(string)
	userId := c.Locals("user_id").(int64)

	request.CustId = custId
	request.ParentCustId = parentCustId
	request.CreatedBy = &userId

	errs := controller.validator.ValidateStruct(request, headerAcceptLang)
	if errs != nil {
		log.Error("OrderController, Create, ValidateStruct, errs:", errs)
		responsePayload.Setmsg(fiber.ErrBadRequest.Message)
		responsePayload.Seterrors(errs)
		return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
	}
	// STORE ORDER
	data, err := controller.OrderService.Store(request)
	if err != nil {
		log.Error("OrderController, Create, Store, err:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
	}

	responsePayload.Setmsg("Created Successfully")
	responsePayload.Setdata(data)
	return c.Status(fiber.StatusCreated).JSON(responsePayload.GetRespPayload())
}

