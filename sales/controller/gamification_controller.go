	gamificationRouteV1.Get("/master/sales-teams", controller.SalesTeamList)
	gamificationRouteV1.Get("/master/operation-types", controller.OperationTypeList)
	gamificationRouteV1.Get("/master/warehouses", controller.WarehouseList)
	gamificationRouteV1.Get("/master/salesmans", controller.SalesmanList)
	gamificationRouteV1.Get("/master/product-categories", controller.ProductCategoryList)
	gamificationRouteV1.Get("/master/product-lines", controller.ProductLineList)
	gamificationRouteV1.Get("/master/brands", controller.BrandList)
	gamificationRouteV1.Get("/master/sub-brands1", controller.SubBrand1List)
	gamificationRouteV1.Get("/master/sub-brands2", controller.SubBrand2List)
	gamificationRouteV1.Get("/master/products", controller.ProductList)
	gamificationRouteV1.Get("/master/outlet-locations", controller.OutletLocationList)
	gamificationRouteV1.Get("/master/outlet-groups", controller.OutletGroupList)
	gamificationRouteV1.Get("/master/outlet-classes", controller.OutletClassList)
	gamificationRouteV1.Get("/master/markets", controller.MarketList)
	gamificationRouteV1.Get("/master/districts", controller.DistrictList)
	gamificationRouteV1.Get("/master/industries", controller.IndustryList)
	gamificationRouteV1.Get("/master/outlets", controller.OutletList)
	gamificationRouteV1.Get("/master/sources", controller.SourceList)
	gamificationRouteV1.Get("/master/measurements", controller.MeasurementList)
	gamificationRouteV1.Get("/master/announcements", controller.AnnouncementList)
