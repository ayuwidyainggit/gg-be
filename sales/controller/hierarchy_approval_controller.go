type HierarchyApprovalController struct {
	HierarchyApprovalService service.HierarchyApprovalService
	validator                *validation.Validate
}

func NewHierarchyApprovalController(HierarchyApprovalService service.HierarchyApprovalService, validator *validation.Validate) *HierarchyApprovalController {
	return &HierarchyApprovalController{
		HierarchyApprovalService: HierarchyApprovalService,
		validator:                validator,
	}
}
func (controller *HierarchyApprovalController) Route(app *fiber.App) {
	qParamId := ":hierarchy_approval_id"
	qParamRequestApprovalId := ":request_approval_id"

	HierarchyApprovalRouteV1 := app.Group("/v1/hierarchy-approval", middleware.JWTProtected())
	HierarchyApprovalRouteV1.Get("/companies", controller.Companies)
	HierarchyApprovalRouteV1.Get("/employees", controller.Employees)

	HierarchyApprovalRouteV1.Post("", controller.Create)
	HierarchyApprovalRouteV1.Post("/request", controller.RequestApproval)
	HierarchyApprovalRouteV1.Get("/request/"+qParamRequestApprovalId, controller.RequestApprovalDetail)

	HierarchyApprovalRouteV1.Get("", controller.List)
	HierarchyApprovalRouteV1.Get("/"+qParamId, controller.Detail)
	HierarchyApprovalRouteV1.Patch("/"+qParamId, controller.Update)
	HierarchyApprovalRouteV1.Delete("/"+qParamId, controller.Delete)
}

func (controller *HierarchyApprovalController) Companies(c *fiber.Ctx) error {
	var dataFilter entity.CompaniesQueryFilter
	var headerAcceptLang string
	if len(c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG]) > 0 {
		headerAcceptLang = c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG][0]
	}
	responsePayload := responsebuild.BuildResponse(c.Locals("requestid").(string), headerAcceptLang)
	if err := c.QueryParser(&dataFilter); err != nil {
		log.Error("InvoiceController, List, query parser filter:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusUnprocessableEntity).JSON(responsePayload.GetRespPayload())
	}
	errs := controller.validator.ValidateStruct(dataFilter, headerAcceptLang)
	if errs != nil {
		log.Error("InvoiceController, Update, ValidateStruct(params), errs:", errs)
		responsePayload.Setmsg(fiber.ErrBadRequest.Message)
		responsePayload.Seterrors(errs)
		return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
	}

	dataFilter.CustId = c.Locals("cust_id").(string)
	dataFilter.ParentCustId = c.Locals("parent_cust_id").(string)

	data, total, lastPage, err := controller.HierarchyApprovalService.Companies(dataFilter)
	if err != nil {
		log.Error("InvoiceController, List, data, err:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
	}

	responsePayload.Setdata(data)
	responsePayload.Setpaging(entity.Pagination{
		TotalRecord: total,
		PageCurrent: 0,
		PageLimit:   0,
		PageTotal:   lastPage,
	})

	return c.Status(fiber.StatusOK).JSON(responsePayload.GetRespPayload())
}
