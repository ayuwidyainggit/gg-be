type ApPaymentController struct {
	ApPaymentService service.ApPaymentService
	validator        *validation.Validate
}

func NewApPaymentController(chequeService service.ApPaymentService, validator *validation.Validate) *ApPaymentController {
	return &ApPaymentController{
		ApPaymentService: chequeService,
		validator:        validator,
	}
}

func (controller *ApPaymentController) Route(app *fiber.App) {
	qParamId := ":account_payable_payment_no"
	grRouteV1 := app.Group("/v1/account-payable-payments", middleware.JWTProtected())
	grRouteV1.Post("", controller.Create)
	grRouteV1.Get("/"+qParamId, controller.Detail)
	grRouteV1.Get("", controller.List)
	grRouteV1.Patch(""+qParamId, controller.Update)
	grRouteV1.Delete("/"+qParamId, controller.Delete)

	grfRouteLookup := app.Group("/v1/account-payable-payments-lookup", middleware.JWTProtected())
	grfRouteLookup.Get("/check-giro", controller.ListBalancePaymentDepositByCustId)
	grfRouteLookup.Get("/bank-transfer", controller.ListBalancePaymentDepositByCustId)
	grfRouteLookup.Get("/cndn", controller.ListBalancePaymentDepositByCustId)
	grfRouteLookup.Get("/return", controller.ListBalancePaymentDepositByCustId)
	grfRouteLookup.Get("/invoice-no", controller.ListInvoiceNo)

}

func (controller *ApPaymentController) Create(c *fiber.Ctx) error {
	var headerAcceptLang string
	if len(c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG]) > 0 {
		headerAcceptLang = c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG][0]
	}
	responsePayload := responsebuild.BuildResponse(c.Locals("requestid").(string), headerAcceptLang)

	var request entity.CreateAccountPayablePaymentBody
	if err := c.BodyParser(&request); err != nil {
		log.Error("ApPaymentController, Create, BodyParser:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusUnprocessableEntity).JSON(responsePayload.GetRespPayload())
	}

	custId := c.Locals("cust_id").(string)
	userId := c.Locals("user_id").(int64)

	request.CustId = custId
	request.CreatedBy = userId

	errs := controller.validator.ValidateStruct(request, headerAcceptLang)
	if errs != nil {
		log.Error("ApPaymentController, Create, ValidateStruct, errs:", errs)
		responsePayload.Setmsg(fiber.ErrBadRequest.Message)
		responsePayload.Seterrors(errs)
		return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
	}

	_, err := controller.ApPaymentService.Store(request)
	if err != nil {
		log.Error("ApPaymentController, Create, Store, err:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
	}
	responsePayload.Setmsg("Created Successfully")
	return c.Status(fiber.StatusCreated).JSON(responsePayload.GetRespPayload())
}

func (controller *ApPaymentController) Detail(c *fiber.Ctx) error {
	var params entity.DetailAccountPayablePaymentParams
	var headerAcceptLang string
	if len(c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG]) > 0 {
		headerAcceptLang = c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG][0]
	}
	responsePayload := responsebuild.BuildResponse(c.Locals("requestid").(string), headerAcceptLang)

	if err := c.ParamsParser(&params); err != nil {
		log.Error("ApPaymentController, Detail, ParamsParser:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusUnprocessableEntity).JSON(responsePayload.GetRespPayload())
	}

	errs := controller.validator.ValidateStruct(params, headerAcceptLang)
	if errs != nil {
		log.Error("ApPaymentController, Detail, ValidateStruct(params), errs:", errs)
		responsePayload.Setmsg(fiber.ErrBadRequest.Message)
		responsePayload.Seterrors(errs)
		return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
	}

	custId := c.Locals("cust_id").(string)
	ParentCustId := c.Locals("parent_cust_id").(string)

	// log.Println("OutletController, Detail, CustId:", custId)

	data, err := controller.ApPaymentService.Detail(params.AccountPayablePaymentNo, custId, ParentCustId)
	if err != nil {
		log.Error("ApPaymentController, Detail, FindOneByOutletId, err:", err.Error())
		statusCode := fiber.StatusBadRequest
		errMsg := err.Error()
		if err.Error() == "sql: no rows in result set" {
			statusCode = fiber.StatusNotFound
			errMsg = "Not found"
		}

		responsePayload.Setmsg(errMsg)
		return c.Status(statusCode).JSON(responsePayload.GetRespPayload())
	}

	responsePayload.Setdata(data)
	return c.JSON(responsePayload.GetRespPayload())
}

