type ApDistributorDiscountController struct {
	ApDistributorDiscountService service.ApDistributorDiscountService
	validator                    *validation.Validate
}

func NewApDistributorDiscountController(mOpexService service.ApDistributorDiscountService, validator *validation.Validate) *ApDistributorDiscountController {
	return &ApDistributorDiscountController{
		ApDistributorDiscountService: mOpexService,
		validator:                    validator,
	}
}

func (controller *ApDistributorDiscountController) Route(app *fiber.App) {
	qParamId := ":distributor_discount_id"
	mOpexRouteV1 := app.Group("/v1/account-payable-discounts", middleware.JWTProtected())
	mOpexRouteV1.Post("", controller.Create)
	mOpexRouteV1.Get("/"+qParamId, controller.Detail)
	mOpexRouteV1.Get("", controller.List)
	mOpexRouteV1.Patch("/"+qParamId, controller.Update)
	mOpexRouteV1.Delete("/"+qParamId, controller.Delete)

}

func (controller *ApDistributorDiscountController) Create(c *fiber.Ctx) error {
	var headerAcceptLang string
	if len(c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG]) > 0 {
		headerAcceptLang = c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG][0]
	}
	responsePayload := responsebuild.BuildResponse(c.Locals("requestid").(string), headerAcceptLang)

	var request entity.CreateApDistributorDiscountBody
	if err := c.BodyParser(&request); err != nil {
		log.Error("ApDistributorDiscountController, Create, BodyParser:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusUnprocessableEntity).JSON(responsePayload.GetRespPayload())
	}

	custId := c.Locals("cust_id").(string)
	userId := c.Locals("user_id").(int64)

	request.CustID = custId
	request.CreatedBy = userId
	request.UpdatedBy = userId

	errs := controller.validator.ValidateStruct(request, headerAcceptLang)
	if errs != nil {
		log.Error("ApDistributorDiscountController, Create, ValidateStruct, errs:", errs)
		responsePayload.Setmsg(fiber.ErrBadRequest.Message)
		responsePayload.Seterrors(errs)
		return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
	}

	err := controller.ApDistributorDiscountService.Store(request)
	if err != nil {
		log.Error("ApDistributorDiscountController, Create, Store, err:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
	}
	responsePayload.Setmsg("Created Successfully")
	return c.Status(fiber.StatusCreated).JSON(responsePayload.GetRespPayload())
}

