type ChequeGiroClearingController struct {
	ChequeGiroClearingService service.ChequeGiroClearingService
	validator                 *validation.Validate
}

func NewChequeGiroClearingController(chequeGiroClearingService service.ChequeGiroClearingService, validator *validation.Validate) *ChequeGiroClearingController {
	return &ChequeGiroClearingController{
		ChequeGiroClearingService: chequeGiroClearingService,
		validator:                 validator,
	}
}

func (controller *ChequeGiroClearingController) Route(app *fiber.App) {
	qParamId := ":cheque_giro_no"
	grRouteV1 := app.Group("/v1/cheque-giro-clearing", middleware.JWTProtected())
	grRouteV1.Get("/", controller.ListReport)
	grRouteV1.Get("/"+qParamId, controller.Detail)
	grRouteV1.Patch("/accept/"+qParamId, controller.Update)
	grRouteV1.Patch("/reject/"+qParamId, controller.UpdateChange)
}

func (controller *ChequeGiroClearingController) ListReport(c *fiber.Ctx) error {
	var dataFilter entity.CheckGiroClearingQueryFilter
	var headerAcceptLang string
	if len(c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG]) > 0 {
		headerAcceptLang = c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG][0]
	}
	responsePayload := responsebuild.BuildResponse(c.Locals("requestid").(string), headerAcceptLang)
	if err := c.QueryParser(&dataFilter); err != nil {
		log.Error("ChequeGiroClearingController, List, query parser filter:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusUnprocessableEntity).JSON(responsePayload.GetRespPayload())
	}
	errs := controller.validator.ValidateStruct(dataFilter, headerAcceptLang)
	if errs != nil {
		log.Error("ChequeGiroClearingController, Create, ValidateStruct, errs:", errs)
		responsePayload.Setmsg(fiber.ErrBadRequest.Message)
		responsePayload.Seterrors(errs)
		return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
	}
	dataFilter.CustId = c.Locals("cust_id").(string)
	dataFilter.ParentCustId = c.Locals("parent_cust_id").(string)
	// log.Println("BankController, List, CustId:", custId)

	data, total, lastPage, err := controller.ChequeGiroClearingService.ListChequeGiroClearing(dataFilter)
	if err != nil {
		log.Error("ChequeGiroClearingController, List, data, err:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
	}

	responsePayload.Setdata(data)
	responsePayload.Setpaging(entity.Pagination{
		TotalRecord: total,
		PageCurrent: dataFilter.Page,
		PageLimit:   dataFilter.Limit,
		PageTotal:   lastPage,
	})

	return c.Status(fiber.StatusOK).JSON(responsePayload.GetRespPayload())
}

func (controller *ChequeGiroClearingController) Detail(c *fiber.Ctx) error {
	var params entity.DetailChequeGiroParams
	var headerAcceptLang string
	if len(c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG]) > 0 {
		headerAcceptLang = c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG][0]
	}
	responsePayload := responsebuild.BuildResponse(c.Locals("requestid").(string), headerAcceptLang)

	if err := c.ParamsParser(&params); err != nil {
		log.Error("ChequeGiroController, Detail, ParamsParser:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusUnprocessableEntity).JSON(responsePayload.GetRespPayload())
	}

	errs := controller.validator.ValidateStruct(params, headerAcceptLang)
	if errs != nil {
		log.Error("ChequeGiroController, Detail, ValidateStruct(params), errs:", errs)
		responsePayload.Setmsg(fiber.ErrBadRequest.Message)
		responsePayload.Seterrors(errs)
		return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
	}

	custId := c.Locals("cust_id").(string)
	// log.Println("OutletController, Detail, CustId:", custId)

	data, err := controller.ChequeGiroClearingService.Detail(params.ChequeGiroNo, custId)
	if err != nil {
		log.Error("ChequeGiroController, Detail, FindOneByOutletId, err:", err.Error())
		statusCode := fiber.StatusBadRequest
		errMsg := err.Error()
		if err.Error() == "sql: no rows in result set" {
			statusCode = fiber.StatusNotFound
			errMsg = "Not found"
		}

		responsePayload.Setmsg(errMsg)
		return c.Status(statusCode).JSON(responsePayload.GetRespPayload())
	}

	responsePayload.Setdata(data)
	return c.JSON(responsePayload.GetRespPayload())
}

