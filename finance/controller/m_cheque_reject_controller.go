type MChequeRejectController struct {
	MChequeRejectService service.MChequeRejectService
	validator            *validation.Validate
}

func NewMChequeRejectController(MChequeRejectService service.MChequeRejectService, validator *validation.Validate) *MChequeRejectController {
	return &MChequeRejectController{
		MChequeRejectService: MChequeRejectService,
		validator:            validator,
	}
}

func (controller *MChequeRejectController) Route(app *fiber.App) {
	qParamId := ":chq_reject_id"
	MChequeRejectRouteV1 := app.Group("/v1/cheque-reject", middleware.JWTProtected())
	MChequeRejectRouteV1.Post("", controller.Create)
	MChequeRejectRouteV1.Get("/"+qParamId, controller.Detail)
	MChequeRejectRouteV1.Get("", controller.List)
	MChequeRejectRouteV1.Patch("/"+qParamId, controller.Update)
	MChequeRejectRouteV1.Delete("/"+qParamId, controller.Delete)

}

func (controller *MChequeRejectController) Create(c *fiber.Ctx) error {
	var headerAcceptLang string
	if len(c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG]) > 0 {
		headerAcceptLang = c.GetReqHeaders()[constant.HEADER_ACCEPT_LANG][0]
	}
	responsePayload := responsebuild.BuildResponse(c.Locals("requestid").(string), headerAcceptLang)

	var request entity.CreateMChequeRejectBody
	if err := c.BodyParser(&request); err != nil {
		log.Error("MMChequeRejectController, Create, BodyParser:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusUnprocessableEntity).JSON(responsePayload.GetRespPayload())
	}

	custId := c.Locals("cust_id").(string)
	userId := c.Locals("user_id").(int64)

	request.CustId = custId
	request.CreatedBy = &userId

	errs := controller.validator.ValidateStruct(request, headerAcceptLang)
	if errs != nil {
		log.Error("MMChequeRejectController, Create, ValidateStruct, errs:", errs)
		responsePayload.Setmsg(fiber.ErrBadRequest.Message)
		responsePayload.Seterrors(errs)
		return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
	}

	err := controller.MChequeRejectService.Store(request)
	if err != nil {
		log.Error("MMChequeRejectController, Create, Store, err:", err.Error())
		responsePayload.Setmsg(err.Error())
		return c.Status(fiber.StatusBadRequest).JSON(responsePayload.GetRespPayload())
	}
	responsePayload.Setmsg("Created Successfully")
	return c.Status(fiber.StatusCreated).JSON(responsePayload.GetRespPayload())
}

